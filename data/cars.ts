export const makes = [...new Set(cars.map((c) => c.make))].sort();
export const fuelTypes: FuelType[] = ["Бензин", "Дизел", "Хибрид", "Електричен", "ЛПГ"];
export const transmissions: TransmissionType[] = ["Рачен", "Автоматик", "Полуавтомат"];
export const bodyTypes: BodyType[] = ["Седан", "Хечбек", "Комбе", "Џип", "Купе", "Кабриолет", "Ван"];

export function getCarBySlug(slug: string): Car | undefined {
  return cars.find((c) => c.slug === slug);
}

export function getAvailableCars(): Car[] {
  return cars.filter((c) => c.status === "available");
}

export function getRelatedCars(car: Car, limit = 3): Car[] {
  return cars
    .filter((c) => c.id !== car.id && c.status === "available" && (c.make === car.make || c.bodyType === car.bodyType))
    .slice(0, limit);
}
